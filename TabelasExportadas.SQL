

DROP TABLE LOG;

CREATE TABLE LOG ( 
ID  INTEGER, 
DATA_HORA  TIMESTAMP, 
EVENTO VARCHAR(50), 
USUARIO VARCHAR(30), 
PERFIL VARCHAR(30), 
NIVEL VARCHAR(10), 
 OUTRAS_INFORMACOES VARCHAR(50) ); 



